package com.example.apk_android_penilaian_hotel.Models;

public class ModelBio {
    private String nama, umur;

    public ModelBio(String nama, String umur) {
        this.nama = nama;
        this.umur = umur;
    }

    public String getNama() {
        return nama;
    }

    public String getUmur() {
        return umur;
    }


}
